
public class ComandosAritmeticos {

	public static void main(String[] args) {
		int numero1 = 20, numero2 = 10, resultado = 0;
		
		resultado = numero1 + numero2;
		System.out.println(resultado);
		
		resultado = numero1 - numero2;
		System.out.println(resultado);
		
		resultado = numero1 * numero2;
		System.out.println(resultado);
		
		resultado = numero1 / numero2;
		System.out.println(resultado);

	}

}
